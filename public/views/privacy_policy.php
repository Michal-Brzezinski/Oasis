        <h1>Polityka prywatności Oasis</h1>

        <p>
            Niniejsza Polityka Prywatności opisuje zasady przetwarzania danych osobowych użytkowników
            aplikacji <strong>Oasis</strong>, służącej do zarządzania inteligentnym ogrodem.
        </p>

        <h2>1. Administrator danych</h2>
        <p>
            Administratorem danych osobowych jest właściciel aplikacji Oasis.
            W sprawach związanych z ochroną danych możesz kontaktować się pod adresem:
            <strong>kontakt@oasis-app.pl</strong>.
        </p>

        <h2>2. Jakie dane przetwarzamy</h2>
        <p>W zależności od sposobu korzystania z aplikacji, możemy przetwarzać:</p>
        <ul>
            <li>adres e-mail</li>
            <li>imię i nazwisko</li>
            <li>hasło (zaszyfrowane)</li>
            <li>dane techniczne urządzenia (np. adres IP, logi serwera)</li>
            <li>dane dotyczące konfiguracji Twojego inteligentnego ogrodu</li>
        </ul>

        <h2>3. Cele przetwarzania danych</h2>
        <p>Dane przetwarzamy w następujących celach:</p>
        <ul>
            <li>utworzenie i obsługa konta użytkownika</li>
            <li>zapewnienie działania aplikacji Oasis</li>
            <li>obsługa zgłoszeń i kontaktu z użytkownikiem</li>
            <li>analiza działania systemu i poprawa jakości usług</li>
        </ul>

        <h2>4. Podstawa prawna przetwarzania</h2>
        <p>
            Dane przetwarzamy zgodnie z RODO, na podstawie:
        </p>
        <ul>
            <li>art. 6 ust. 1 lit. b – wykonanie umowy (korzystanie z aplikacji)</li>
            <li>art. 6 ust. 1 lit. f – prawnie uzasadniony interes administratora</li>
            <li>art. 6 ust. 1 lit. a – zgoda użytkownika (np. newsletter)</li>
        </ul>

        <h2>5. Przechowywanie danych</h2>
        <p>
            Dane przechowujemy tak długo, jak jest to konieczne do realizacji celów,
            dla których zostały zebrane, lub do momentu usunięcia konta.
        </p>

        <h2>6. Prawa użytkownika</h2>
        <p>Masz prawo do:</p>
        <ul>
            <li>dostępu do swoich danych</li>
            <li>sprostowania danych</li>
            <li>usunięcia danych („prawo do bycia zapomnianym”)</li>
            <li>ograniczenia przetwarzania</li>
            <li>przenoszenia danych</li>
            <li>wniesienia sprzeciwu wobec przetwarzania</li>
        </ul>

        <h2>7. Bezpieczeństwo danych</h2>
        <p>
            Stosujemy odpowiednie środki techniczne i organizacyjne, aby chronić dane
            przed nieautoryzowanym dostępem, utratą lub uszkodzeniem.
        </p>

